<p>Now, if you have gotten this far with no problems, let's learn one more chord:</p>

<p>This is a new one, called D. Here is how it looks...can you figure it out?</p>
		
<pre>
        E  A  D  G  B  E
		X  X  0  2  3  2
</pre>

<p>Which then looks like this:</p>

<pre>
		EADGBE

		XX0232
</pre>

<p>And, finally looks like this:</p>

<pre>
		XX0232
</pre>

<p>(Remember, the X means we don't strum that string. And, the 0 means "open," which means what we play that string without touching it with our fingers. If this is suddenly confusing, no problem. Just go back up closer to the top of this page for a review of the X and 0.)</p>

<p>I would like for you to learn D because it is used a LOT. Here it is again:</p>

<pre>
    This is the top of your guitar.

    These are your strings.

    big strings<<....>>little strings
	E    A    D    G    B    E
	X    X    0                 (X = don't play)(0 = open)
         
	..........................  1st fret of guitar
         
	...............1.........2  2nd fret of guitar
         
	....................3.....  3rd fret of guitar
</pre>

<p>So, here's a written explanation, as I'd really like you to learn this chord easily. Let's take it string-by-string, just to make sure:</p>

<p>Let's begin with the large E string, the one that is on the left of the chart. It's also the string closest to left side of the neck.</p>

<p>E string: You do not play or touch this string. It's an X.</p>

<p>A string: You do not play or touch this string. It's an X.</p>

<p>D string: You play this string, but do not touch it. It's an 0.</p>

<p>G string: Place your index finger (that's finger #1) on the 2nd fret.</p>

<p>B string: Place your ring finger (that's finger #3) on the 3rd fret.</p>

<p>E string: Place your middle finger (that's finger #2) on the 2nd fret.</p>

<p>I hope that this makes the D chord easier to understand.</p>

<p>Now, give it a soft stroke. Remember, you're only strumming 4 strings, the D, G, B and E. You don't play the big E or A. And, the D string is played "open"...you don't touch it with your fingers.</p>

<p>Sounds kind of nice, right?</p>

<p>Now, let's have some fun with it...right now you are playing it on the 2nd fret, right?</p>

<p>Now, play it on the 7th fret. Sounds cool.</p>

<p>Now, play it on the 9th fret. Sounds cool, too.</p>

<p>Now, play it on the 9th fret, then slide it down to the 7th fret and play it.</p>

<p>This may sound a little confusing, but it will make sense later:</p>

<p>When you played the D on the 7th fret, it made it a G chord....and on the 9th fret it is an A chord.</p>

<p>That's right, when you played that regular D chord on the 9th fret it becomes an A chord just like the X X 2 2 2 X chord. They sound a lot alike. That tiny change is what makes the difference.</p>

<p>If you're still with me, try playing the D chord on the 14th fret... guess what, it's a D again. Try playing it on different frets, especially the 2nd, 4th, 5th, 7th, 9th, 14th, etc...just slide it around....now, that's kind of what "slider" chords are all about.</p>

<p>Did you try it on the 4th fret? Sounds like that E chord you learned first, right? Yep, it's an E. (Again, it's not an "official" E, but it's close enough for our lesson, right? Right!)</p>

<p>Now, hold it. If you play D on the 4th fret, it's an E, right? So, if you played that same D on the 9th fret, it would be an A. And, if you played it on the 11th fret, it would be a B. Well, uh, those are the same chords (E, A and B) that we learned for "Wild Thing."</p>

<p>So, if you play the D chord on the 4th, 9th and 11th frets, it's like playing "Wild Thing"?</p>

<p>Sure...not exactly Hendrix, and not *exactly* E, A and B, but close enough to pass for it.</p>

<p>So, try it.</p>

<p>First, play the E, A and B that you learned.</p>

<p>Now, play the regular D on the 4th, 9th and 11th frets.</p>

<p>That's right, it fits!</p>

<p>So, now you know 2 ways to play "Wild Thing."</p>

<p>OK, you're right. Using our basic E, A and B combination does sound a lot more like "Wild Thing" than using the D chords.</p>

<p>The D chord sounds, well, more "friendly" and more "acoustic."</p>

<p>I call chords like our D, "sliders," because you can slide them up and down the neck to make all kinds of neat chords.</p>

<p>Here's something that I forgot to tell you...check this out:</p>

<p>Did some of those D chords played on the 7th fret, 5th fret, etc., sound kind of familiar? Those are the EXACT same chords that the Eagles use on their guitars to play "Hotel California." That's right. And, I bet that you thought that "Hotel California" was really hard to play...hey, you're playing a big hunk of it right now. No sweat, right?</p>

<p>In fact, if you combine the basic E chord that we learned with the D chord and add just one more chord, then you can play "Hotel California." And, that is not a "shortcut" method. They are the EXACT chords in the EXACT positions that the Eagles use in concert when they play it.</p>

