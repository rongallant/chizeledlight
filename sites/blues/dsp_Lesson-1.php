<h1>Lesson 1</h1>

<p>This lesson begins with a letter I had received from a new guitar player. I had helped him to find, choose, and purchase a 1964 Fender Stratocaster guitar and set it up properly. As I had done appraisals in the past, I helped him to verify the authenticity of the instrument, which was purchased for about $3,500.00.</p>

<p>He had learned to tune it and had written me to say "Thanks" and had commented that he was starting to use "Tab," a form of music tablature that assigns numbers to various notes.</p>

<p>I had said this in my answer to his email:</p>

<p>
&gt;&gt; If you are already using tab, then you can play the notes to whatever you want..cool. If you need help with chords, etc., just let me know, ok?
The new player replied:
&gt;&gt; Yeah.. What's a chord? I'm going to guess that it's when you play a note that uses more than one string at the same time...
Whoa!  This was a real wake-up call for me. I guess that I had forgotten that some new players need some basic information and either don't have immediate resources available or are reluctant to ask.
</p>

<p>So, for those of you who are just starting out, here's my reply to the "What's a chord and how do you play one" question.</p>

<p>I will also explain how chords are made, how they are "charted" (reproduced on paper), the ways of reading them and the ways of playing them.</p>

<p>Then, we'll finish the lesson by learning a song with different chords and the basics of how to manipulate the chords.</p>

<p>It's really a lot easier than it sounds, so let's try it, ok?</p>

<p>Let's begin with a quick review of the "fretboard" and "frets." The fretboard is the slightly-rounded, almost flat surface on the top of your guitar neck. It has thin metal strips running across the top of it. Those are "frets."</p>

<p>A "fret" is a thin strip of metal that divides your neck into 21 to 24 different sections. The 1st fret is the one nearest where you tune your strings. The higher frets, like the 15th or 20th, etc., are very near the body of the guitar.</p>

<p>You do NOT place your fingers on the strips...you place your fingers between them and press down onto the wooden fretboard.</p>

<p>For example, if you are asked to put your finger on the 4th fret, your fingertip would be placed on the fretboard, directly behind the 4th fret on the wooden surface between the 3rd and 4th fret.</p>

<p>A chord is when you place several fingers on several specific places on the fretboard and then strum several of the strings at once. All songs are made of chords and there are easily thousands of chords.  The good news is that rock/blues use only a few basic chords.</p>

<p>When you play a number of single notes...one at a time...that is called playing a "lead" or "solo."</p>

<p>Here's your basic guitar chord...it is called E.</p>

<p>In the documentary "Hail, Hail Rock and Roll," Keith Richards (who also does not know scales, theory or how to read music) calls E the basic guitar chord, because your guitar is tuned in E.</p>

<p>Here's how an E looks:</p>

<pre>
		E  022100
</pre>

<p>If you don't know chords, that's a bummer, as it's hard to figure this out.  But, if you read on, you'll be playing it within five minutes.</p>

<p>Let's take a look at what 022100 means:</p>

<p>Those six numbers, 022100, represent the six strings on your guitar and where each finger should be placed to form an E chord.</p>

<p>Start on the left, that is, the big fat string. That's the E string, as it is tuned to E. So, we read it from left to right.</p>

<p>If we were looking at your guitar from the front, here are the strings and the notations:</p>

<pre>
	(up this way is where you tune your 
                strings)^^^

		  E  A  D  G  B  E
		  0  2  2  1  0  0

	(down in this direction is the body of the guitar)
</pre>

<p>The E on the far left is the big string. The E on the far right is the little string.</p>

<p>Now, for the 022100 part. Let's go from left to right:</p>

<p>The 0 means "open," that is, the string is open...none of your fingers are even touching it...just like an 0 in tab.</p>

<p>The 2 right next to the 0 means that you are pressing down on the 2nd fret of the guitar on the A string (it's the one next to the E..see diagram above).</p>

<p>So, in our case, 2 means that you have a finger pressing down on the second fret of the A string. Remember that our "2" means the 2nd fret, not your 2nd finger (we'll cover that later).</p>

<p>The next 2 is on the D string. So, you place a fingertip on the second fret of the D string. Remember, when you "place a fingertip on the second fret" it means that you press your fingertip down onto the string so that it touches the fretboard between the 2nd fret and the 3rd fret.</p>

<p>Don't press down on the actual strip of metal which is called the fret because it is made of fretwire.</p>

<p>Next comes a 1. So, you place a fingertip on the first (1) fret of that string, which is the G string.</p>

<p>The next two are both 0. So, you don't touch them at all. You strum them, but you do not touch them.</p>

<p>Press down hard enough with your fingertips so that you can hear all of the strings clearly. If one makes a "thud" noise, then you either need to press down a little bit harder or maybe another finger is touching the string.</p>

<p>Just try it until all six strings make a clear noise. There is no reason to strum hard. Just use a soft stroke with your pick or your thumb.</p>

<p>If this is brand new to you, your fingertips may hurt a tiny bit. That is because they've never done this before. Once they get used to it, not only will they not hurt, they'll even enjoy it!</p>

<p>Strum again. It they all don't sound clear, don't get discouraged. Just re-adjust your fingers and try again. There's a first time for everything and this is one of those times. Just try once more.</p>

<p>Don't get frustrated...again, there's a first time for everything.</p>

<p>See...each time you try it, it gets easier...and that's a scientific fact. If you believe me, skip the next paragraph. If you don't believe me, just read the next paragraph, but only if you keep strumming the E chord while you do it:</p>

<p>The complexity of intermuscular relationships combined with a reflective stimulus, such as a sound, triggers a combination of both visual and neuro-muscular coordination, thus allowing for both sides of the brain to assimilate corridors of initialization and implementation of successful repetitive skills. It's also why it is so much easier to remember the words and music of a song, than to learn a 20-item grocery list. I know.....boring.....I apologize. I hope you are still strumming.</p>

<p>Now, you might be wondering "Hey, which finger goes on which string?"</p>

<p>That's a darn good question! You could twist up your fingers trying to figure this out...</p>

<p><p>Let's give it a try:</p></p>

<p>As we have already learned and strummed, 022100 is how E is shown.</p>

<p>That's a drawback of this form of showing chords...it doesn't tell you which finger goes on which fret of which string. You are supposed to place them in a way that is most comfortable for you.</p>

<p>Let's take our E chord and learn where to put our fingertips:</p>

<p>Here's how we'd do this one above, the E chord = 022100:</p>

<p>Your left index finger goes on the 1st fret of the G string.</p>

<p>Your middle finger goes on the 2nd fret of the A string.</p>

<p>Your ring finger goes on the 2nd fret of the D string.</p>

<p>The other strings are played open...you don't touch them or mute them.</p>

<p>Give it a try. At first, you'll probably have to press down a little harder or move a fingertip a tiny bit to make it sound clean...but, that's normal.</p>

<p>There's no reason to strum hard. Use a pick if you have one...this is easier than getting a sore thumb. Hold the pick loosely...this will be an important control feature in the future.</p>

<p>Do most guitar players hold picks loosely? Yes. That's why they have 10 or 12 taped to a mic stand in front of them...if one flies off, they can grab another one fast.</p>

<p>Now, put your fingers in position for your E chord. If you have forgotten which fingers go where, just read a couple of paragraphs above this one and review it. If that still doesn't help, then look down about 10 lines and you'll see a graphic of where to put your fingers.</p>

<p>Give it a strum...a nice, clean, easy sweep. It should sound clean and basic. If not, make minor adjustments with your fingertips and make sure that a finger is not touching the wrong string by mistake.</p>

<p>Now, try it again...it's worth the effort...trust me on this one.</p>

<p>You might have to do this several times, but that's ok. That's how you learn. Remember, every guitar player on earth had this same problem when they were first beginning to play. You are not alone.</p>

<p>Can you play it? Can you softly strum it? Use soft, downward strokes.</p>

<p>Try it a few more times. If you're feeling brave, lift you fingers off of the fretboard and try it again from scratch. Can you put your fingertips in the right place without having to refer to this email? If so, cool. You've learned your first chord! You're a natural!</p>

<p>Now, please don't think "It took me 15 minutes to do this...how am I going to learn 100s of chords?"</p>

<p>Let me show you just how easy it is to learn a new chord:</p>

<p>Put your fingers in position to play your E chord. Now, strum it.</p>

<p>Sounding good!</p>

<p>Now, keep everything exactly where it is, but lift up your index finger so that it does not touch the fretboard.</p>

<p>That should look like this: 022000</p>

<p>Give it a strum...that's an Em or E minor. Notice the little difference between the E (022100) and the Em (022000)?
(I just tossed that in to show how easy it is to make and change chords...E and Em are not much of a change at all.)</p>

<p>If you play rock or blues or metal, you'll use the E chord perhaps more than any other. So, if you have learned it by now, you're cookin'!</p>

<p>But, maybe you haven't quite figured out the 022100 thing yet. I don't blame you...it is confusing. Maybe this will help to clarify things:</p>

<p>Let's take another look at our E chord:</p>

<p>I going to try to show you an E chord as it you were looking straight at your guitar neck:</p>

<pre>
	You tune your guitar up here.
	
	These are your strings.

	big strings<<..>>little strings
	E    A    D    G    B    E
         
	...............X.......... 1st fret of guitar
         
	.....X....X............... 2nd fret of guitar
         
	.......................... 3rd fret of guitar
</pre>

<p>The X is where a fingertip should be.</p>

<p>Notice that there is no X on the big E, B or E strings.</p>

<p>Notice that you put a fingertip on the 2nd fret of the A string.</p>

<p>Notice that you put a fingertip on the 2nd fret of the D string.</p>

<p>Notice that you put a fingertip on the 1st fret of the G string.</p>

<p>So, the notation would be like this:</p>

<pre>
		E  A  D  G  B  E
		0  2  2  1  0  0
</pre>

<p>Now, your guitar strings will almost always be:</p>
 
<pre>
		E  A  D  G  B  E
</pre>

<p>because that is the standard tuning of a guitar. So, to save time, it is written as:</p>

<pre>
		0  2  2  1  0  0
</pre>

<p>and, so to save space, it is then written as 022100.</p>

<p>But, what if you really want or need to know which finger goes where? Good question. What we do is number your fingers...</p>

<p>your index finger is 1</p>

<p>your middle finger is 2</p>

<p>your ring finger is 3</p>

<p>your pinky finger is 4</p>

<p>So, now we can show the same E chord, except now show exactly which finger goes where:</p>

<pre>
	    You tune your guitar up here.
	          These are your strings.

	      big strings<<..>>little strings
		E    A    D    G    B    E
         
		...............1..........  1st fret of guitar
         
		.....2....3...............  2nd fret of guitar
         
		..........................  3rd fret of guitar
         
	  The body of your guitar is down this way.
</pre>

<p>See, it's really kind of easy.</p>

<p>But, you can also see that when you assign numbers to your fingers, plus numbers for the frets, it can get really confusing.</p>

<p>So, almost no one uses numbers for fingers anymore. But, don't "fret" about it. You can really use any fingers you want. It depends on how much easier it is to play and, later on, whether you'll be switching to another chord and want to minimize finger movements.</p>

<p>Now, let me give you a notation and you try to name it and play it....here it is:</p>

<pre>
		022100
</pre>

<p>That's right. It's an E and you did fine. Now, let's learn a song...this could be the chords to "Wild Thing":</p>

<p>(No, this is not the absolute perfect way to play these chords.... each one could be played many ways...this is just to make it easy.)</p>

<p>OK, so here are our simplified chords for "Wild Thing":</p>

<pre>
		E   022100

		A   XX222X

		B   XX444X
</pre>

<p>What does the X mean? It means that you don't play that string.</p>

<p>Just forget about the other 3 strings.</p>

<p>See the A chord? For now, just use your index, middle and ring fingers to cover the 3 strings. Or, if you can, try to play all three notes with just your index finger. You will have to learn this sooner or later, you might want to start now. Don't worry that your index finger "mutes" or touches the small E string...it's ok.</p>

<p>So "Wild Thing" would go like this:</p>

<pre>
		E E...AA...BB...AA
</pre>

<p>over and over again.</p>

<p>Did that work for you? Good. But, the A and B chords sounded a little bit off.</p>

<p>That's because you just playing 3 strings. Many chords do not use all 6 strings on the guitar and our "easy" A and B are two of them.</p>

<p>Sometimes, instead of an X, you will see an M. The M means Mute. To "Mute" a string means that you simply don't play it, or use one of your fingers to press against the string so that it doesn't make any noise. A light touch will do it.</p>

<p>Either way, same thing.</p>

<p>So, our "Wild Thing" chords really look like this:</p>

<pre>
		E  0 2 2 1 0 0
 
		A  X X 2 2 2 X

		B  X X 4 4 4 X
</pre>

<p>And, we would strum it like this:</p>

<pre>
		E E   A A   B B   A A
</pre>

<p>over and over again.</p>


<p>"The Troggs" or Hendrix, we still have it like this:</p>

<pre>
		E.......E......A..A......B..B.....A..A
		Wild thing......

		..............E.....E....A..A....B..B.....A..A
		You make my heart sing......

		...........E.....E....A..A....B..B....A..A
		You make everything...........groovy

		E.......E......A..A....B..B..B..B
		Wild thing.........
</pre>

<p>Now, these are "simplified" forms of A and B to make it easier... we can always learn better and more "official" ways later. The bottom line is that now you know how to read chords and how to play them.</p>

<p>Yes, you can now make a chord! Play a chord! Change chords!</p>

<p>See, that wasn't so bad, huh? I knew you could do it!!</p>

