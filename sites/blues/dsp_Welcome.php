<h1>Welcome</h1>

<p>Welcome to <b>Jim's Blues School</b>, the new guitar player's homepage.</p>

<p>If you would like to learn to play the blues easily and quickly, without long and boring academic lectures on "theory," the "physics of dynamic intervals" and other boring stuff, then you've come to the right place.</p>

<p>Hi, I'm Jim, and I'll be your teacher and your guide. I've been playing blues and rock guitar for almost 40 years and I am a certified teacher with two Masters degrees.  My work has been published in magazines like Guitar Player, and I have been teaching for almost 20 years.</p> 

<p>I've taught hundreds of students of all ages at all levels from beginner through advanced and we've all had a great time.</p>

<p>If you are interested in playing blues, but don't have a lot of experience doing it, pick up your guitar and get ready for some easy-to-follow lessons.</p> 

<p>These are not tiny "sample" lessons. Each lesson takes from one to three hours to complete, but you'll find it time well spent.</p>  

<p>For you more experienced blues players, we have over 200 blues-related links to audio, video, bios, tab, gear and homepages. From Luther Allison to ZZ Top, you'll find it here. Plus, our 200 links lead to over 1,000 more!</p>

<p>So, grab your guitar, sit back, get comfortable, and get ready to learn to play some of your favorite rock and blues tunes.</p>

<p>Thanks for dropping by and have a great time!</p>

<p>Sincerely,</p>

